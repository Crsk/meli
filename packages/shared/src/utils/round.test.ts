import { round } from './round'

describe('round', () => {
  test.each`
    value               | decimals | expected
    ${9876.04652342345} | ${4}     | ${9876.0465}
    ${785207.5678635}   | ${4}     | ${785207.5679}
    ${12.345}           | ${4}     | ${12.345}
    ${7.8}              | ${4}     | ${7.8}
    ${42}               | ${4}     | ${42}
    ${9876.54}          | ${4}     | ${9876.54}
    ${9876.0467543}     | ${3}     | ${9876.047}
    ${785207.5}         | ${3}     | ${785207.5}
    ${12.345}           | ${3}     | ${12.345}
    ${7.8}              | ${3}     | ${7.8}
    ${42}               | ${3}     | ${42}
    ${9876.54}          | ${3}     | ${9876.54}
    ${9876.04678643}    | ${2}     | ${9876.05}
    ${785207.5}         | ${2}     | ${785207.5}
    ${12.345}           | ${2}     | ${12.35}
    ${7.8}              | ${2}     | ${7.8}
    ${42}               | ${2}     | ${42}
    ${9876.54}          | ${2}     | ${9876.54}
    ${9876.04}          | ${1}     | ${9876.0}
    ${785207.5}         | ${1}     | ${785207.5}
    ${12.345}           | ${1}     | ${12.3}
    ${7.8}              | ${1}     | ${7.8}
    ${42}               | ${1}     | ${42}
    ${9876.54}          | ${1}     | ${9876.5}
    ${9876.04}          | ${0}     | ${9876}
    ${785207.5}         | ${0}     | ${785208}
    ${12.345}           | ${0}     | ${12}
    ${7.8}              | ${0}     | ${8}
    ${42}               | ${0}     | ${42}
    ${9876.54}          | ${0}     | ${9877}
  `('returns the correct value', ({ value, decimals, expected }) => {
    expect(round(value, decimals)).toBe(expected)
  })
})
